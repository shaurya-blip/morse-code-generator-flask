
import subprocess
import time

def to_morse_code(text):
    code = {'a': ".-", 'b': "-...", 'c': "-.-.", 'd': "-..", 'e': ".",
            'f': "..-.", 'g': "--.", 'h': "....", 'i': "..", 'j': ".---", 'k': "-.-",
            'l': ".-..", 'm': "--", 'n': "-.", 'o': "---", 'p': ".--.", 'q': "--.-",
            'r': ".-.", 's': "...", 't': "-", 'u': "..-", 'v': "...-", 'w': ".--",
            'x': "-..-", 'y': "-.--", 'z': "--..",
            '1': ".----", '2': "..---", '3': "...--", '4': "....-", '5': ".....",
            '6': "-....", '7': "--...", '8': "---..", '9': "----.", '0': "-----",
            ' ': "/", '.': ".-.-.-", ',': "--..--", '?': "..--..", "'": ".----.",
            '@': ".--.-.", '-': "-....-", '"': ".-..-.", ':': "---...", ';': "---...",
            '=': "-...-", '!': "-.-.--", '/': "-..-.", '(': "-.--.", ')': "-.--.-",
            'á': ".--.-", 'é': "..-..", '\r': '',  '\n': '', '\t': ''}

    morse_code = ""

    for x in text:
        morse_code += code[x.lower()]

    return morse_code



def to_morse(morse):

    text = to_morse_code(morse)

    for char in text:
        if char == '-':
            subprocess.call(['afplay', 'dah.wav'])
        if char == ".":
            subprocess.call(['afplay', 'dit.wav'])
        elif char == ' ':
            time.sleep(0.7)
        else:
            pass

